#!/usr/bin/env bash
# Remote deploy — invoked only from GitHub Actions CI via SSH.
set -euo pipefail

DEPLOY_DIR="${DEPLOY_DIR:-/opt/chrisvouga-dev}"
SERVICE_ID="${1:-}"
IMAGE_TAG="${IMAGE_TAG:-latest}"

cd "${DEPLOY_DIR}"

image_owner() {
  awk '/^image_owner:/ { print $2; exit }' services.yaml
}

image_prefix() {
  awk '
    /^image_prefix:/ { print $2; exit }
    /^zone:/ { zone=$2; gsub(/\./, "-", zone); print zone; exit }
  ' services.yaml
}

is_infra_service() {
  local id="$1"
  awk -v id="$1" '
    /^infra_services:/ { in_infra=1; next }
    in_infra && /^[a-zA-Z_]/ { in_infra=0 }
    in_infra && /^  - id: / {
      line=$0
      sub(/^  - id: /, "", line)
      gsub(/ *$/, "", line)
      if (line == id) { found=1; exit }
    }
    END { exit(found ? 0 : 1) }
  ' services.yaml
}

service_image() {
  local svc="$1"
  docker compose config --images "${svc}" 2>/dev/null | head -1
}

image_exists_locally() {
  local image="$1"
  [[ -n "${image}" ]] && docker image inspect "${image}" >/dev/null 2>&1
}

# Stop anything holding host port 80 before traefik recreate (avoids bind races).
free_host_port() {
  local port="$1"
  local ids names
  ids="$(docker ps -q --filter "publish=${port}" 2>/dev/null || true)"
  [[ -z "${ids}" ]] && return 0
  names="$(docker ps --filter "publish=${port}" --format '{{.Names}}' | tr '\n' ' ')"
  echo "==> Stopping containers on port ${port}: ${names}"
  docker stop ${ids}
  docker rm -f ${ids} 2>/dev/null || true
}

pull_service() {
  local svc="$1"
  if [[ "${svc}" == "service-orchestrator" ]]; then
    echo "---- skip pull ${svc} (built on node)"
    return 0
  fi

  local image
  image="$(service_image "${svc}")"

  if docker compose pull "${svc}"; then
    return 0
  fi

  if image_exists_locally "${image}"; then
    echo "WARN: pull failed for ${svc}, using cached image ${image}" >&2
    return 0
  fi

  return 1
}

always_on_services() {
  awk '
    function flush_runtime() {
      if (id != "" && runtime == "always_on") print id
      id = ""
      runtime = "on_demand"
    }
    /^services:/ { section = "app"; next }
    /^infra_services:/ { section = "infra"; next }
    /^[a-zA-Z_]/ && !/^  / { section = ""; flush_runtime(); next }
    section != "" && /^  - id: / {
      flush_runtime()
      line = $0
      sub(/^  - id: /, "", line)
      gsub(/ *$/, "", line)
      id = line
      next
    }
    section != "" && id != "" && /^    runtime: always_on/ { runtime = "always_on"; next }
    END { flush_runtime() }
  ' services.yaml
}

up_always_on_services() {
  local -a services=("$@")
  local -a rest=()
  local has_traefik=false

  for svc in "${services[@]}"; do
    if [[ "${svc}" == "traefik" ]]; then
      has_traefik=true
    else
      rest+=("${svc}")
    fi
  done

  if [[ "${has_traefik}" == true ]]; then
    free_host_port 80
    docker compose stop traefik 2>/dev/null || true
    docker compose rm -f traefik 2>/dev/null || true
    echo "---- up traefik"
    docker compose up -d traefik
  fi

  if [[ ${#rest[@]} -gt 0 ]]; then
    echo "---- up ${rest[*]}"
    docker compose up -d "${rest[@]}"
  fi
}

deploy_all_services() {
  local always_on_failed=()
  local svc

  echo "==> Pull always-on services (IMAGE_TAG=${IMAGE_TAG})"
  local pull_targets=(traefik)
  while IFS= read -r svc; do
    [[ -z "${svc}" ]] && continue
    [[ "${svc}" == "traefik" ]] && continue
    pull_targets+=("${svc}")
  done < <(always_on_services)

  for svc in "${pull_targets[@]}"; do
    echo "---- pull ${svc}"
    if ! pull_service "${svc}"; then
      always_on_failed+=("${svc}")
      echo "WARN: pull failed for ${svc}" >&2
    fi
  done

  echo "==> Build service-orchestrator"
  docker compose build service-orchestrator

  echo "==> Up always-on services"
  local up_services=()
  for svc in "${pull_targets[@]}"; do
    if [[ " ${always_on_failed[*]:-} " == *" ${svc} "* ]]; then
      echo "---- skip ${svc} (pull failed, no cached image)"
      continue
    fi
    up_services+=("${svc}")
  done

  up_always_on_services "${up_services[@]}"

  if [[ ${#always_on_failed[@]} -gt 0 ]]; then
    echo "ERROR: failed to pull always-on service(s): ${always_on_failed[*]}" >&2
    echo "       publish the image and ensure the ghcr package is public" >&2
    echo "       run: bun run make-ghcr-public" >&2
    exit 1
  fi
}

if [[ -n "${SERVICE_ID}" ]]; then
  COMPOSE_SERVICE="${SERVICE_ID}"
  OWNER="$(image_owner)"
  PREFIX="$(image_prefix)"

  if [[ "${IMAGE_TAG}" != "latest" ]] && ! is_infra_service "${SERVICE_ID}"; then
    IMAGE="ghcr.io/${OWNER}/${PREFIX}-${SERVICE_ID}:${IMAGE_TAG}"
    cat > docker-compose.override.yml <<EOF
# AUTO-GENERATED by scripts/deploy.sh — do not commit
services:
  ${COMPOSE_SERVICE}:
    image: ${IMAGE}
EOF
  else
    rm -f docker-compose.override.yml
  fi

  if [[ "${COMPOSE_SERVICE}" == "service-orchestrator" ]]; then
    echo "==> Build ${COMPOSE_SERVICE}"
    docker compose build "${COMPOSE_SERVICE}"
  else
    echo "==> Pull ${COMPOSE_SERVICE} (tag=${IMAGE_TAG})"
    pull_service "${COMPOSE_SERVICE}" || {
      echo "ERROR: pull failed for ${COMPOSE_SERVICE}" >&2
      exit 1
    }
  fi

  if [[ "${COMPOSE_SERVICE}" == "traefik" ]]; then
    free_host_port 80
    docker compose stop traefik 2>/dev/null || true
    docker compose rm -f traefik 2>/dev/null || true
  fi

  echo "==> Up ${COMPOSE_SERVICE}"
  docker compose up -d "${COMPOSE_SERVICE}"
  docker compose logs --tail=40 "${COMPOSE_SERVICE}" 2>&1 || true
else
  rm -f docker-compose.override.yml
  export IMAGE_TAG
  deploy_all_services
fi

docker compose ps
